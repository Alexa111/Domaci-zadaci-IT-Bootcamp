package sport;

public class Clan {
	
	private String  ime;
	
	private String  prezime;
	
	private int  godiste;


	public Clan(String  ime, String  prezime, int godiste) {
		super();
		this.ime = ime;
		this.prezime = prezime;
		this.godiste = godiste;
	}

	public String getIme() {
		return ime;
	}

	public String getPrezime() {
		return prezime;
	}

	public int getGodiste() {
		return godiste;
	}


	

	
	


	
	
	
}
